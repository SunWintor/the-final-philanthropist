type LuckyBoy struct {
	heroInfo
}

func (l *LuckyBoy) Init() {
	l.Id = 3
	l.Rarity = SSRRarity
	l.MoneyLimit = 100
	l.CurrentMoney = l.MoneyLimit
	l.Name = "天选之人"
	l.Motto = "如此幸运其实很无聊的"
	l.SkillInfo = []SkillInfo{{
		Name: "空投砸脸",
		Desc: "每回合开始时获得1枚金币"},
	}
}

func (l *LuckyBoy) OnRoundStart(ctx *SkillContext) {
	if l.IsBankrupt() {
		return
	}
	l.AddMoney(1)
}
